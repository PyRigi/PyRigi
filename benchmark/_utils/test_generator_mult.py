from typing import List, Dict, Any, Optional
from pathlib import Path

from _utils import dataset_loader


def generate_conftest_file(
    benchmark_dir: str,
    timeout_seconds: float = None,
    timeout_threshold: float = None,
    nodes_total: Dict[Any, int] = None,
) -> str:
    """
    Generate a conftest.py in the benchmark directory containing:
    - pytest_benchmark_update_json hook (strips raw stats.data)
    - timeout_seconds fixture (injects per-test timeout value)
    - timeout_tracker session fixture (tracks timeouts & early stops across tests)
    - pytest_sessionfinish hook (writes timeout log file)

    Args:
        benchmark_dir: Directory where conftest.py will be written.
        timeout_seconds: Per-test timeout in seconds, or None to disable.
        timeout_threshold: Ratio at which to early stop, or None to disable.
        nodes_total: Dict mapping (config_key, num_nodes) to total run count.

    Returns:
        Absolute path to the generated conftest.py.
    """
    output_path = str(Path(benchmark_dir) / "conftest.py")

    timeout_repr = repr(timeout_seconds)
    threshold_repr = repr(timeout_threshold)
    nodes_total_repr = repr(nodes_total) if nodes_total else "{}"

    content = f'''\
# Auto-generated by benchmarkapp.py — do not edit manually.
# Deleted automatically after each benchmark run.
import json
import os
import pytest


_TIMEOUT_SECONDS = {timeout_repr}
_TIMEOUT_THRESHOLD = {threshold_repr}
_NODES_TOTAL = {nodes_total_repr}

# Global tracking arrays
_global_timeout_tracker = {{
    "counts": {{}},
    "stopped": {{}},
    "stops_info": [],
    "threshold": _TIMEOUT_THRESHOLD,
    "nodes_total": _NODES_TOTAL,
    "timeout_log": [],
}}


def pytest_benchmark_update_json(config, benchmarks, output_json):
    """
    Strip raw per-round timing arrays (stats.data) before pytest-benchmark
    writes the JSON results file.
    """
    for bench in output_json.get("benchmarks", []):
        bench.get("stats", {{}}).pop("data", None)


@pytest.fixture
def timeout_seconds():
    """Return the configured per-test timeout (None = no timeout)."""
    return _TIMEOUT_SECONDS


@pytest.fixture(scope="session")
def timeout_tracker():
    """Return the global dictionary for tests to mutate."""
    return _global_timeout_tracker


def pytest_sessionfinish(session, exitstatus):
    """Write timeout log to a JSON file at the end of the session."""
    timeout_file = os.environ.get(
        "BENCHMARK_TIMEOUT_LOG", os.path.join("{benchmark_dir}", "timeout_log.json")
    )
    with open(timeout_file, "w") as f:
        json.dump(
            {{
                "timed_out": _global_timeout_tracker["timeout_log"],
                "timeout_seconds": _TIMEOUT_SECONDS,
                "early_stops": _global_timeout_tracker["stops_info"],
            }},
            f, indent=2
        )
'''

    with open(output_path, "w") as f:
        f.write(content)

    return output_path


def generate_benchmark_test_file(
    func_path: str,
    func_name: str,
    graph_param_name: str,
    dataset_paths: List[str],
    configurations: List[Dict[str, Any]],
    output_path: str = "benchmark/temp_benchmark_test.py",
    explicit_combinations: Optional[List[tuple]] = None,
) -> tuple[str, dict]:
    """
    Generate a pytest file using parametrization to capture config values in JSON output.
    Pre-loads all graphs to handle multi-graph .g6 files correctly.

    Args:
        explicit_combinations: Optional list of (config, graph_info) tuples to run.
                              If provided, skips Cartesian product generation.
    """

    file_abs_path = func_path.split(":")[0]
    tq = '"""'  # triple-quote helper

    print("Pre-loading graphs from dataset files...")
    all_graphs = dataset_loader.load_graph_infos(dataset_paths, func_name, 20)
    print(f"Loaded {len(all_graphs)} graphs from {len(dataset_paths)} files")

    # Parameter combinations: (config_dict, graph_metadata)
    param_combinations = []

    if explicit_combinations:
        print(f"Using {len(explicit_combinations)} explicit combinations/tasks.")
        param_combinations = explicit_combinations
    else:
        # Default: Cartesian product of configs x graphs
        print(
            f"Generating Cartesian product of {len(configurations)} configs x "
            f"{len(all_graphs)} graphs."
        )
        for config in configurations:
            for graph_info in all_graphs:
                param_combinations.append((config, graph_info))

    # Pre-compute total test count per (config, num_nodes) for early stop
    nodes_total = {}
    for config, graph_info in param_combinations:
        config_key = str(dict(sorted(config.items())))
        n = graph_info["num_nodes"]
        key = f"{config_key}|{n}"
        nodes_total[key] = nodes_total.get(key, 0) + 1

    # Build complete test file with parametrization
    content = f"""import pytest
import signal
import networkx as nx
import sys
import importlib.util

# Dynamic Import
try:
    spec = importlib.util.spec_from_file_location("target_module", r"{file_abs_path}")
    module = importlib.util.module_from_spec(spec)
    sys.modules["target_module"] = module
    spec.loader.exec_module(module)
    target_func = getattr(module, "{func_name}")
except Exception as e:
    raise RuntimeError(f"Failed to import function inside generated test: {{e}}")


# --- Timeout support ---
_HAS_SIGALRM = hasattr(signal, 'SIGALRM')

class BenchmarkTimeout(Exception):
    pass

def _timeout_handler(signum, frame):
    raise BenchmarkTimeout("BENCHMARK_TIMEOUT")
# --- End timeout support ---


# Parametrized Benchmark
@pytest.mark.parametrize("config,graph_info", {repr(param_combinations)})
def test_benchmark(benchmark, config, graph_info, timeout_seconds, timeout_tracker):
    {tq}
    Benchmark the target function with given config and graph.
    pytest-benchmark will automatically capture 'config' and 'graph_info' in JSON output.

    graph_info contains: file_path, file_idx, graph_idx, file_name, num_nodes, num_edges
    {tq}
    # Extract metadata
    file_path = graph_info['file_path']
    graph_idx = graph_info['graph_idx']
    n = graph_info['num_nodes']
    config_key = str(dict(sorted(config.items())))

    # 1. Early Stop Check
    stopped_at = timeout_tracker["stopped"].get(config_key)
    if stopped_at is not None and n >= stopped_at:
        pytest.skip(
            f"EARLY_STOP: n={{n}} skipped "
            f"(config stopped at n={{stopped_at}})"
        )

    # Load graph from file
    graph_data = nx.read_graph6(file_path)

    # Handle both single graph and multiple graphs in file
    if hasattr(graph_data, '__iter__') and not isinstance(graph_data, nx.Graph):
        graphs = list(graph_data)
        graph = graphs[graph_idx]
    else:
        graph = graph_data

    # Prepare kwargs with graph parameter
    kwargs = config.copy()
    kwargs['{graph_param_name}'] = graph

    # 2. Timeout guard
    if timeout_seconds is not None and _HAS_SIGALRM:
        old_handler = signal.signal(signal.SIGALRM, _timeout_handler)
        signal.alarm(int(timeout_seconds))
        try:
            benchmark(target_func, **kwargs)
        except BenchmarkTimeout:
            # Register timeout and check for early stop
            timeout_tracker["timeout_log"].append(
                f"BENCHMARK_TIMEOUT: Timed out after {{timeout_seconds}}s "
                f"(nodes={{n}}, file={{graph_info['file_name']}})"
            )

            if timeout_tracker["threshold"] is not None:
                key = f"{{config_key}}|{{n}}"
                timeout_tracker["counts"][key] = (
                    timeout_tracker["counts"].get(key, 0) + 1
                )
                count = timeout_tracker["counts"][key]
                total = timeout_tracker["nodes_total"].get(key, 1)

                if (
                    (count / total) >= timeout_tracker["threshold"]
                    and config_key not in timeout_tracker["stopped"]
                ):
                    timeout_tracker["stopped"][config_key] = n

                    # Store info for reporting
                    timeout_tracker["stops_info"].append({{
                        "config": config,
                        "stopped_at_num_nodes": n,
                        "timeout_count": count,
                        "total_count": total,
                        "ratio": count / total,
                        "threshold": timeout_tracker["threshold"]
                    }})

                    # Print immediate live banner
                    print(f"\\n{'=' * 60}")
                    print(f" EARLY STOP TRIGGERED: {{config}} stopped at n={{n}}")
                    print(
                        f" {{count}}/{{total}} = {{(count/total)*100:.1f}}% "
                        f"timed out "
                        f"(threshold: {{timeout_tracker['threshold']*100:.1f}}%)"
                    )
                    print(f" All tests with n>={{n}} for this config will be skipped.")
                    print(f"{'=' * 60}\\n")

            pytest.skip(
                f"BENCHMARK_TIMEOUT: Timed out after {{timeout_seconds}}s "
                f"(nodes={{n}}, file={{graph_info['file_name']}})"
            )
        finally:
            signal.alarm(0)
            signal.signal(signal.SIGALRM, old_handler)
    elif timeout_seconds is not None and not _HAS_SIGALRM:
        import warnings
        warnings.warn(
            "signal.SIGALRM not available on this platform; "
            "per-test timeout is not enforced.",
            RuntimeWarning,
            stacklevel=1,
        )
        benchmark(target_func, **kwargs)
    else:
        # No timeout - original behavior
        benchmark(target_func, **kwargs)
"""
    Path(output_path).parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w") as f:
        f.write(content)

    return output_path, nodes_total
